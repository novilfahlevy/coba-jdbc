    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
       if(txtNim.getText().equals("")) {
           JOptionPane.showMessageDialog(this, "Pilih Data yang ingin Dihapus");
       } else {
           int conf = JOptionPane.showConfirmDialog(null, "Menghapus data ini", "?", JOptionPane.YES_NO_OPTION);
           if (conf == 0) {
               try {
                st = db.connection.createStatement();
                String sql = "DELETE FROM data_mahasiswa WHERE NIM = '" + txtNim.getText() + "'";
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
                showData();
                nullValue();
               } catch (Exception e) {
                   JOptionPane.showMessageDialog(null, e);
               }
               
           }
       }
    }//GEN-LAST:event_btnHapusActionPerformed

