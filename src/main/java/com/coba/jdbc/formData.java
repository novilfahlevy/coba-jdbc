    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        System.out.println("Test");
        try {
            st = db.connection.createStatement();
            if (txtNim.getText().equals("") || txtNama.getText().equals("") ||
                    txtProdi.getText().equals("") || txtFakultas.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Data Tidak boleh Kosong", "",  JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            
            if (btnAdd.getText().equals("Simpan")) {
                String check = "SELECT * FROM data_mahasiswa WHERE NIM = '" + txtNim.getText() + "'";
                rs = st.executeQuery(check);
                
                if(rs.next()) {
                    JOptionPane.showMessageDialog(null, "Data Sudah ada");
                } else {
                    String sql = "INSERT INTO data_mahasiswa VALUES ('" +
                            txtNim.getText() + "','" +
                            txtNama.getText() + "','" +
                            txtProdi.getText() + "','" +
                            txtFakultas.getText() + "')";
                    st.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, "Data Tersimpan");
                    nullValue();
                    setVisible(false);
                    dispose();
                    new formData().setVisible(true);
                }
            } else {
                String update = "UPDATE data_mahasiswa SET nama = '" + txtNama.getText()
                        + "', program_studi = '" + txtProdi.getText() 
                        + "', fakultas = '" + txtFakultas.getText() 
                        + "' WHERE NIM = '" + txtNim.getText() + "'";
                
                st.executeUpdate(update);
                JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
                nullValue();
                showData();
                        
            }
                
        } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
    }//GEN-LAST:event_btnAddActionPerformed

