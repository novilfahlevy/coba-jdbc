    private void showData() {
        try {
            st = db.connection.createStatement();
            rs = st.executeQuery("SELECT * FROM data_mahasiswa");
            
            DefaultTableModel model = new DefaultTableModel();
            
            model.addColumn("No");
            model.addColumn("NIM");
            model.addColumn("Nama");
            model.addColumn("Program Studi");
            model.addColumn("Fakultas");
            
            int no = 1;
            
            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();
            model.setRowCount(0);
            
            while (rs.next()) {
                Object[] data = {
                    no++,
                    rs.getString("NIM"),
                    rs.getString("nama"),
                    rs.getString("program_studi"),
                    rs.getString("fakultas")
                };
                model.addRow(data);
                tblData.setModel(model);
            }
            
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Tidak dapat Ditampilkan");
        }
    }
    
